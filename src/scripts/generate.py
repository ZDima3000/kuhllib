#!/usr/bin/python

import sys
import re

inc_re  = re.compile(r"#include <(.*)>")
comp_re = re.compile(r"#include <(.*).hpp>")
name_re = re.compile(r"namespace")
end_re  = re.compile(r"#endif")

headers = {}

def get_std_headers(comp):
    with open("include/" + comp + ".hpp") as file:
        for line in file:
            inc_match  = inc_re.match(line)
            comp_match = comp_re.match(line)
            if inc_match and not comp_match:
                headers[inc_match.group(1)] = 1

def get_declarations(comp):
    with open("include/" + comp + ".hpp") as file:
        for line in file:
            if name_re.match(line):
                print(line)
                break

        for line in file:
            if end_re.match(line):
                break
            print(line[:-1])

