comps = sys.stdin.readlines()
comps = comps[::-1]
for comp in comps:
    comp = comp[:-1]
    get_std_headers(comp)

for header in headers.keys():
    print("#include <" + header + ">")

for comp in comps:
    comp = comp[:-1]
    get_declarations(comp)

